package modulo.ejercicios.uno;

public class PatronesDeDiseño {

    public static void main(String[] args) {

        /*
        Patrones de diseño.

        Patrones creacionales:
        Patrón 'singleton': Permite asegurar que una clase tenga una única instancia, a la vez que proporciona un
        punto de acceso global a dicha instancia. Su uso se aplica cuando una clase de un determinado programa tan solo
        debe tener una instancia disponible para todos los clientes; por ejemplo, un único objeto de base de datos
        compartido por distintas partes del programa.

        Patrón 'factory': Proporciona una interfaz para crear objetos en una superclase, mientras permite a las
        subclases alterar el tipo de objetos que se crearán. Su uso se aplica cuando se quiera ofrecer a los usuarios
        de una determinada biblioteca o framework, una forma de extender sus componentes internos.

        Patron 'builder': Permite construir objetos complejos paso a paso. El patrón permite producir distintos tipos
        y representaciones de un objeto empleando el mismo código de construcción. Su uso se aplica cuando se quiera
        que el código sea capaz de crear distintas representaciones de ciertos productos.

        Patrón 'prototype': Permite copiar objetos existentes sin que el código dependa de sus clases. Su uso se aplica
        cuando un determinado código no deba depender de las clases concretas de objetos que se necesiten copiar.

        Patrones estructurales:
        Patrón 'decorator': Permite añadir funcionalidades a objetos colocando estos objetos dentro de objetos
        encapsuladores especiales que contienen estas funcionalidades. Se uso se aplica cuando se necesite asignar
        funcionalidades adicionales a objetos durante el tiempo de ejecución sin descomponer el código que utiliza
        esos objetos.

        Patrón 'adapter': Permite la colaboración entre objetos con interfaces incompatibles. Su uso se aplica cuando
        se quiera usar una clase existente, pero cuya interfaz no sea compatible con el resto del código.

        Patrón 'facade': Permite proporcionar una interfaz simplificada a una biblioteca, un framework o cualquier otro
        grupo complejo de clases. Su uso se aplica cuando se necesite una interfaz limitada pero directa a un
        subsistema complejo.
         */

    }

}
